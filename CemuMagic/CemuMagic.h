static std::string slist2str(const struct curl_slist* l)
{
    std::string result;
    while(true)
    {
        if(l == nullptr)
            break;
        if(!result.empty())
            result += ", ";
        result += "\"";
        result += l->data;
        result += "\"";
        l = l->next;
    }
    if(result.empty())
        result = "null";
    return result;
}

